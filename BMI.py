input("Калкулатор на индекса на телесна маса\n Моля въведете тегло и ръст\n Натиснете Enter, за да продължите...")

weight = float(input("Тегло в килограми:")) #тегло в килограми 

height = float(input("Височина в метри: ")) # височина в метри

BMI = weight / (height ** 2) # изчисляване на BMI
print() #празен ред за по-добра четимост
print(f"Вашият индекс на телесна маса е: {BMI:.2f}")


